export function renderReadme(name: string): string {
  return README_TEMPLATE.split(README_PLACEHOLDER).join(name);
}

export function isKebabCase(name: string): boolean {
  return /^[a-z][a-z0-9]*(-[a-z0-9]+)*$/.test(name);
}

export function isValidTemplateKind(value: string): value is TemplateKind {
  return value === 'extension' || value === 'trigger' || value === 'module';
}

function entryPoint(kind: TemplateKind): string {
  if (kind === 'module') {
    return `// Module entry point\nexport const version = '0.1.0';\n`;
  }
  const signature =
    kind === 'trigger'
      ? 'const { operation, record, oldRecord } = ctx;\n  fm.utils.log(`Trigger: ${operation} on ${ctx.objectApiName}`);'
      : 'fm.utils.log(`Trigger: ${ctx.operation}`);';
  return `export default async function handler(ctx: fm.TriggerContext) {\n  ${signature}\n  return { success: true };\n}\n`;
}

function testFile(kind: TemplateKind): string {
  return `describe('${kind}', () => {\n  it('should pass', () => {\n    expect(true).toBe(true);\n  });\n});\n`;
}

function tsConfig(): string {
  return JSON.stringify(
    {
      compilerOptions: {
        target: 'ES2022',
        module: 'ESNext',
        moduleResolution: 'bundler',
        strict: true,
        outDir: './dist',
        rootDir: './src',
      },
      include: ['src/**/*.ts', 'types/fm.d.ts'],
    },
    null,
    2,
  );
}

function generateManifest(name: string, kind: TemplateKind): string {
  return `[extension]\nname = "${name}"\nversion = "0.1.0"\ndescription = "A Fluxomind ${kind}"\nauthor = "unknown"\nengine_version = "0.1.0"\n\n[permissions]\ndata = ["read"]\n`;
}

interface TemplateFile {
  path: string;
  content: string;
}

export function buildTemplateFiles(name: string, kind: TemplateKind): TemplateFile[] {
  const files: TemplateFile[] = [
    { path: 'manifest.toml', content: generateManifest(name, kind) },
    { path: 'src/index.ts', content: entryPoint(kind) },
    { path: 'tests/index.test.ts', content: testFile(kind) },
    { path: 'tsconfig.json', content: tsConfig() },
    { path: 'types/fm.d.ts', content: '/// <reference types="@fluxomind/sdk" />\n' },
    { path: '.gitignore', content: GITIGNORE_TEMPLATE },
    { path: 'README.md', content: renderReadme(name) },
  ];
  if (kind === 'module') {
    files.splice(2, 0, { path: 'src/objects.toml', content: '# Define custom objects for this module\n' });
  }
  return files;
}

/**
 * Create a new project on disk. Idempotent: fails if targetDir/name exists unless `force: true`.
 */
export function createProject(input: CreateProjectInput): CreateProjectResult {
  if (!isKebabCase(input.name)) {
    throw new ScaffoldError(
      'INVALID_NAME',
      `Invalid project name "${input.name}". Use kebab-case (e.g., my-extension).`,
    );
  }

  if (!isValidTemplateKind(input.type)) {
    throw new ScaffoldError(
      'INVALID_TYPE',
      `Invalid template type "${input.type}". Use: extension, trigger, module.`,
    );
  }

  const parentDir = input.targetDir ?? process.cwd();
  const projectDir = join(parentDir, input.name);

  if (existsSync(projectDir) && !input.force) {
    throw new ScaffoldError('DIR_EXISTS', `Directory "${input.name}" already exists.`);
  }

  try {
    mkdirSync(projectDir, { recursive: true });

    const templateFiles = buildTemplateFiles(input.name, input.type);
    const writtenPaths: string[] = [];

    for (const file of templateFiles) {
      const filePath = join(projectDir, file.path);
      mkdirSync(join(filePath, '..'), { recursive: true });
      writeFileSync(filePath, file.content, 'utf-8');
      writtenPaths.push(file.path);
    }

    return { path: projectDir, files: writtenPaths };
  } catch (err) {
    if (err instanceof ScaffoldError) throw err;
    throw new ScaffoldError(
      'WRITE_FAILED',
      `Failed to scaffold project: ${err instanceof Error ? err.message : String(err)}`,
    );
  }
}
