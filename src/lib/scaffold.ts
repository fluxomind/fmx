/**
 * Scaffold helper — creates a new extension/module/trigger project structure on disk.
 *
 * Shared between:
 *   - `fmx init` (packages/cli/src/commands/init.ts) — CLI command
 *   - MCP scaffold-adapter (src/engine/codeEngine/mcp/scaffold-adapter.ts) — MCP tool
 *     `codeengine_scaffold` invoked locally by AI clients.
 *
 * Pure filesystem operation — no HTTP, no token required. Idempotency guarded by
 * `existsSync(targetDir)` unless `force: true`.
 *
 * @package @fluxomind/cli (local helper)
 */

import { mkdirSync, writeFileSync, existsSync } from 'fs';
import { join } from 'path';

export type TemplateKind = 'extension' | 'trigger' | 'module';

export interface CreateProjectInput {
  /** Project name — must be kebab-case [a-z][a-z0-9]*(-[a-z0-9]+)* */
  name: string;
  /** Project template kind */
  type: TemplateKind;
  /** Parent directory where `<name>/` will be created (default: process.cwd()) */
  targetDir?: string;
  /** If true, skip existsSync check (overwrites). Default false. */
  force?: boolean;
}

export interface CreateProjectResult {
  path: string;
  files: string[];
}

export class ScaffoldError extends Error {
  readonly code: 'INVALID_NAME' | 'INVALID_TYPE' | 'DIR_EXISTS' | 'WRITE_FAILED';

  constructor(code: 'INVALID_NAME' | 'INVALID_TYPE' | 'DIR_EXISTS' | 'WRITE_FAILED', message: string) {
    super(message);
    this.name = 'ScaffoldError';
    this.code = code;
  }
}

